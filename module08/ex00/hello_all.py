#!/usr/bin/env python3

def hello():
    print("Hello, everyone!")

hello()


# def hello():
# Aqui está o conceito central deste módulo: definição de função.

# def é a palavra-chave que diz "estou criando uma função".
# hello é o nome que estou dando pra essa função — é assim que vou chamar ela depois.
# () são os parênteses dos parâmetros — aqui estão vazios porque essa função não recebe nenhum valor de entrada.
# : abre o bloco de código da função, que precisa estar indentado (no caso, a linha do print).